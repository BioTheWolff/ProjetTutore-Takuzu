<div id="ButduJeu">
    <h3 class="sous-session">
        But du jeu :
    </h3>
    <p class="text">
        remplir la grille à l'aide de 0 et de 1.
    </p>
</div>

<div id="regles">
    <h3 class="sous-session">Les règles à respecter :</h3>
    <ul>
        <li>Il n'y a pas plus de deux 0 ou deux 1 consécutifs</li>
        <li>Chaque ligne et chaque colonne contiennent autant de 0 que de 1</li>
        <li>Deux lignes ou deux colonnes ne peuvent être identiques</li>
    </ul>
</div>